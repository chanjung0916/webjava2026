// 최대값 찾기 함수
// 주어진 정수 배열에서 가장 큰 값을 찾아 반환하는 함수입니다.
// 배열은 비어있지 않으며, 음수와 양수가 섞여 있을 수 있습니다.

function findMax(nums) {
    // 첫 번째 요소를 초기 최대값으로 설정합니다.
    // 배열이 비어있지 않으므로 안전합니다.
    let max = nums[0];
    
    // 두 번째 요소부터 배열의 끝까지 반복합니다.
    for (let i = 1; i < nums.length; i++) {
        // 현재 요소가 현재 최대값보다 크면 최대값을 업데이트합니다.
        if (nums[i] > max) {
            max = nums[i];
        }
    }
    
    // 최종적으로 찾은 최대값을 반환합니다.
    return max;
}

// 예시 테스트
console.log(findMax([3, 1, 4, 1, 5])); // 출력: 5
console.log(findMax([-1, -2, -3]));   // 출력: -1